package OOP.Inheritance;

public class ElectricFan { 
    // ElectricFan is the Super CLASS and
    // the classes whos extending to this Class
    // is a subclass
    // (Fan1, Fan2, Fan3) are sub Classes
    
    public void switch1()
    {
        System.out.println("Fan Speed: 1");
    }

    public void switch2()
    {
        System.out.println("Fan Speed: 2");
    }

    public void switch3()
    {
        System.out.println("Fan Speed: 3");
    }

    public void switch4()
    {
        System.out.println("Fan Speed: 4");
    }
}
